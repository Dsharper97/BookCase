    // the fragment initialization parameters, e.g. ARG_ITEM_NUMBER
    public static final String ARG_BOOKS = "books";

    // TODO: Rename and change types of parameters
    private ArrayList<String> books;
    ArrayList<Fragment> fragments;
    ViewPager vp;

    private OnFragmentInteractionListener mListener;

    public ViewPagerFragment() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @return A new instance of fragment ViewPagerFragment.
     */
    // TODO: Rename and change types and number of parameters
    public static ViewPagerFragment newInstance(Bundle b) {
        ViewPagerFragment fragment = new ViewPagerFragment();
        fragment.setArguments(b);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            books = getArguments().getStringArrayList(ARG_BOOKS);
        }
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View v = inflater.inflate(R.layout.fragment_view_pager, container, false);

        for (int i = 0; i < books.size(); i++) {
            Fragment bdf = BookDetailsFragment.newInstance(books.get(i));
            fragments.add(bdf);
        }
        ViewPagerAdapter vpa = new ViewPagerAdapter(getFragmentManager(),fragments);

        vp = v.findViewById(R.id.vp);
        vp.setAdapter(vpa);
        //vp.setAdapter(new ViewPagerAdapter(getChildFragmentManager()));

        return v;
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

